"""Fixed domain-agnostic prompt for trajectory goal generation."""

GOAL_GENERATION_PROMPT_TEMPLATE = """你是用户最终期望结果提取器。
请基于完整 Agent 交互轨迹，综合提炼当前用户最终希望获得的结果。
只输出 JSON，不要输出 Markdown。

抽取原则：
- 以用户消息为主要依据，助手回复和工具结果只用于理解上下文与指代关系。
- 综合整条轨迹，识别用户最终想得到什么结果，而不是简单复述最后一句。
- goal 应表达“最终希望获得的结果 + 必要达成条件”，条件必须服务于结果成立。
- 从用户的补充、纠正、否定、追问中提炼条件、偏好、限制和必要达成条件。
- 如果用户使用代词、序号、简称或省略表达，可以结合上文恢复其真实指向。
- 如果用户表达取消、放弃、暂停或结束，也要作为最终期望结果的一部分。
- 不要把工具偶然返回值、执行状态、内部路径、错误码、日志或中间过程写进 goal。

写作要求：
- goal 必须是一段中文自然语言，表达要自然、顺滑、可读。
- goal 描述“用户最终想获得什么”，不要写成系统操作步骤或僵硬的规章制度。
- 可以自然带出必要条件、偏好、限制和结果成立前需要满足的前置信息。
- 不要机械使用“系统应当”“必须”“禁止”等命令式口吻。
- 不要评价助手哪里做错了，不要给优化建议，不要输出评分结论。
- 提示词中没有任何固定业务领域，输出时只能依据轨迹内容提炼。

输出 JSON schema:
{
  "goal": "一段自然、完整的中文最终期望结果描述",
  "reason": "简短说明提炼依据",
  "confidence": 0.9
}

轨迹：
{messages}
"""
